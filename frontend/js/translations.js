// Função principal de tradução
function t(chave) {
  const lang = localStorage.getItem('applus_idioma') || 'pt';
  const traducoes = TRANSLATIONS[lang] || TRANSLATIONS['pt'];
  return traducoes[chave] || TRANSLATIONS['pt'][chave] || chave;
}

// Aplicar traduções em todos os elementos data-i18n
function aplicarTraducoes() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const chave = el.getAttribute('data-i18n');
    const attr = el.getAttribute('data-i18n-attr');
    if (attr) {
      el.setAttribute(attr, t(chave));
    } else {
      el.textContent = t(chave);
    }
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    el.placeholder = t(el.getAttribute('data-i18n-ph'));
  });
}

// Mostrar tela de seleção de idioma (só na primeira vez)
function verificarIdioma() {
  const idioma = localStorage.getItem('applus_idioma');
  if (!idioma) {
    document.getElementById('idioma-page').style.display = 'flex';
    document.getElementById('login-page').style.display = 'none';
    return true;
  }
  aplicarTraducoes();
  return false;
}

function selecionarIdioma(lang) {
  document.querySelectorAll('.idioma-btn').forEach(b => b.classList.remove('ativo'));
  document.querySelector('[data-lang="' + lang + '"]').classList.add('ativo');
  window._idiomaEscolhido = lang;
}

function confirmarIdioma() {
  const lang = window._idiomaEscolhido || 'pt';
  localStorage.setItem('applus_idioma', lang);
  document.getElementById('idioma-page').style.display = 'none';
  document.getElementById('login-page').style.display = 'flex';
  aplicarTraducoes();
}

function trocarIdioma(lang) {
  localStorage.setItem('applus_idioma', lang);
  aplicarTraducoes();
  alerta('✅ ' + t('idioma.continuar'));
}
